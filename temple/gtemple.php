</span><img src="../images/20161202100115.jpg" width="285" height="261" align="right"></p>

<p><span style='font-family:Arial'>&nbsp;</span mstheme></p>
<p><strong><span
style='mso-bidi-font-size:10.0pt;color:navy'>On the greatest mountain Siddhatek
  which is located on the bank of river Bhima &amp; surrounded by forest and
  trees. God Vishnu did hard penance when he was in great difficulty. He got a
  boon from Lord Siddheshwar Ganesh who is a father or creator of
  `Panchamahabhuta&#8217; (the five elements namely Earth, Water, Fire, Air &amp; Sky)
  and killed the two demons Madhu &amp; Kaitabha.</span></strong><span
style='font-family:Arial'><o:p></o:p>
</span></p>
<p><strong><span style='mso-bidi-font-size:10.0pt;color:navy'>Lord
Siddhivinayaka, the third Ganesh of Ashtavinayaka has trunk towards right and
has authority to complete the work. At this place God Vishnu got Siddhi (divine
illumination). The God is awaked or potent. Sage Moraya Gosavi from Chinchwad
and Sage Narayan Maharaj from Kodgaon got siddhi from the Lord Siddhivinayaka
at this place. Shri Haripant Phadke a general from Peshwa got back his post
when he did penance continuously for 21 days .<O:P> </O:P></span></strong><span
style='font-family:Arial'><o:p></o:p></span></p>

<p align=center style='text-align:center'><b><span style='font-size:13.5pt;
mso-bidi-font-size:10.0pt;color:navy'>How to reach Sidhatek<O:P></span></b><span
style='font-family:Arial'><o:p></o:p></span></p>

<p><strong><span style='mso-bidi-font-size:10.0pt;font-family:Arial;color:navy'>Location
: Siddhatek, Ahmednagar District.</span></strong><span style='font-family:Arial'><o:p></o:p></span></p>

<p><strong><span style='mso-bidi-font-size:10.0pt;font-family:Arial;color:navy'>Road:
Off the Pune-Solapur Highway. 48 km from Srigonda; 19 km from Daund.</span></strong><b><span
style='mso-bidi-font-size:10.0pt;font-family:Arial;color:navy'><br>
<strong>Rail: Alight at Boribyal station on the Pune-Solapur line. The temple
stands about Eleven km from this station, across the river Bhima. Regular State
Transport buses ply from the station. One could also alight at Daund and take a
bus.</strong></span></b></p>

<p><strong><span style='mso-bidi-font-size:10.0pt;font-family:Arial;color:navy'>1)By
rail Pune-Sholapur, one can get down at Daund station to reach the Siddhatek . <O:P></span></strong></p>

<p><strong><span style='mso-bidi-font-size:10.0pt;font-family:Arial;color:navy'>Pune-Daund
is 78 km. The way is Pune-Hadapsar-Loni-Vawat-Chaufula-Patas-Daud-Siddhatek.
Daud-Siddhatek distance is 18 km. One can got to Daud-Shirapur-Bhima river. One
can cross the river with a boat. Private buses, cars are allowed to go till the
bank of river Bhima. After passing the river one has to walk some distance to
reach temple.<O:P> </O:P></span></strong><span style='font-family:Arial'><o:p></o:p></span></p>

<p><strong><span style='mso-bidi-font-size:10.0pt;font-family:Arial;color:navy'>2)By
road one can go from Daud-Kasti-Padgaon to Siddhatek.<O:P> </O:P></span></strong><span
style='font-family:Arial'><o:p></o:p></span></p>

<p><strong><span style='mso-bidi-font-size:10.0pt;font-family:Arial;color:navy'>3)State
transport bus service available between Shivaji Nagar(Pune) to Siddhatek.</span></strong><span
style='color:yellow'>&nbsp;<O:P> </O:P></span><b><span style='font-family:Arial'><o:p></o:p></span></b></p>

<p align=center style='text-align:center'><b><span style='font-size:13.5pt;
mso-bidi-font-size:10.0pt;color:navy'>Stories about Shree Sidhatek<O:P> </O:P></span></b><span
style='font-family:Arial'><o:p></o:p></span></p>

<p><strong><span style='mso-bidi-font-size:10.0pt;font-family:Arial;color:navy'>Long
ago God Brahma thought of creating the nature. For that he did ceaseless
reiteration of single alphabet OM. Lord Ganesha was delighted with the penance
and gave him a boon that he will fulfil his wish of creating the nature. When
God Brahma was creating the nature, God Vishnu felt asleep. Demons Madhu &amp;
Kaitabh immerge from God Vishnu&#8217;s ears (means from his irascible) they started
harassing all gods, goddesses and sages. God Brahma realise that only God
Vishnu will be able to kill the two demons. God Brahma along with other gods
requested God Vishnu to kill the demons. God Vishnu fought with them for 5
thousand years, but he could not succeed in ending their lives. Then God Vishnu
realise that he won&#8217;t succeed unless he worships Lord Ganesha. God Shiva and
goddess Parvati gave him `Ganesh Mantra&#8217; and asked him to go to Siddhi
Shetra(place) for worshipping the mantra. God Vishnu came to Siddhi Shetra.
where he found</span><span style='mso-bidi-font-size:10.0pt;
font-family:Verdana;mso-bidi-font-family:Arial;color:navy'> </span><span
style='mso-bidi-font-size:10.0pt;font-family:Arial;color:navy'>self-existent
right trunk Ganesh idol. God Vishnu installed the idol at Siddhi Shetra and
called the Ganesh as `Siddhi Vinayak&#8217;. This Siddhi Shetra later became famous
by Siddhatek.<O:P> </O:P></span></strong><span style='font-family:Arial'><o:p></o:p></span></p>

<p><strong><span style='mso-bidi-font-size:10.0pt;font-family:Arial;color:navy'>God
Brahma got the energy to create the nature with the boon. With the Lords
blessing God Brahma got two daughters</span><span
style='mso-bidi-font-size:10.0pt;font-family:Verdana;mso-bidi-font-family:Arial;
color:navy'> </span><span style='mso-bidi-font-size:10.0pt;
font-family:Arial;color:navy'>named Siddhi &amp; Buddhi. God Brahma worshipped
Lord Ganesha and requested to accept his daughters as Lord Ganesha&#8217;s wife.<O:P>
</O:P></span></strong><span style='font-family:Arial'><o:p></o:p></span></p>

<p><strong><span style='mso-bidi-font-size:10.0pt;font-family:Arial;color:navy'>Lord
Vishnu, sage Vyas, sage Bhrushundi, sage Moraya Gosavi, sage Narayan Maharaj
were stayed at this holy place. This place is one of the twenty-one places of
Lord Ganesha, which mentioned in the purana (mythology). This place is second
in Ashtavinayaka.<O:P> </O:P></span></strong><span style='font-family:Arial'><o:p></o:p></span></p>

<p align=center style='text-align:center'><strong><span style='mso-bidi-font-size:
10.0pt;font-family:Arial'>The Temple </span></strong><span
style='font-family:Arial'>
  <o:p></o:p></span></p>

<O:P>

<p><strong><span style='mso-bidi-font-size:10.0pt;font-family:Arial;color:navy'>At
Siddhatek Lord Ganesha gave darshan to God Vishnu (Lord Ganesh himself appeared
in front of God Vishnu). God Vishnu built a temple over there with installing
Ganesha idol. The temple has four big entrances. Later on this temple was
destroyed by wear and tear. Years later a cowherd had a divine appearance of
God and saw this temple. That cowherd used to wash the idol by the water of
river Bhima and used to give offering and used to worship. Soon everyone in the
vicinity came to know</span></strong><span
style='font-family:Arial'><o:p></o:p></span></p>

<p align=center style='text-align:center'><b><span style='font-size:13.5pt;
mso-bidi-font-size:10.0pt;color:navy'>Idol<O:P> </O:P></span></b><span
style='font-family:Arial'><o:p></o:p></span></p>

<p><strong><span style='mso-bidi-font-size:10.0pt;font-family:Arial;color:navy'>Lord
Siddhi Vinayaka is having trunk towards right side. Because of this one has to
be very particular about the holiness of the idol. After taking bath, only few
selected people/priests can worship the idol by touching it. No one else is
even allowed to enter the main</span></strong><span
style='font-family:Arial'><o:p></o:p></span></p>

